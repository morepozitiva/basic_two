let number = prompt('Enter the number from 0 to 9', 0);

if(number == 0){
    document.write('0 - )');
} else if(number == 1){
    document.write('1 - !');
} else if(number == 2){
    document.write('2 - @');
} else if(number == 3){
    document.write('3 - #');
} else if(number == 4){
    document.write('4 - $');
} else if(number == 5){
    document.write('5 - %');
} else if(number == 6){
    document.write('6 - ^');
} else if(number == 7){
    document.write('7 - &');
} else if(number == 8){
    document.write('8 - *');
} else if(number == 9){
    document.write('9 - (');
}